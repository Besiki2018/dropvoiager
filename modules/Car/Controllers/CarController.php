    public function index(Request $request)
    {

        $layout = setting_item("car_layout_search", 'normal');
        if ($request->query('_layout')) {
            $layout = $request->query('_layout');
        }
        $is_ajax = $request->query('_ajax');
        $for_map = $request->query('_map',$layout === 'map');

        if(!empty($request->query('limit'))){
            $limit = $request->query('limit');
        }else{
            $limit = !empty(setting_item("car_page_limit_item"))? setting_item("car_page_limit_item") : 9;

        }
        $input = $request->input();
        $pickupPayload = $request->input('pickup');
        if (is_string($pickupPayload)) {
            $decodedPickup = json_decode($pickupPayload, true);
            if (json_last_error() === JSON_ERROR_NONE) {
                $pickupPayload = $decodedPickup;
            }
        }
        if (!is_array($pickupPayload)) {
            $pickupPayload = [];
        }

        $pickupLocationId = Arr::get($pickupPayload, 'id') ?? $request->input('pickup_location_id');
        $selectedPickupLocation = null;
        if ($pickupLocationId) {
            $selectedPickupLocation = TransferLocation::find($pickupLocationId);
            if ($selectedPickupLocation && $selectedPickupLocation->is_active) {
                $pickupPayload = array_merge($selectedPickupLocation->toFrontendArray(), $pickupPayload);
                $pickupPayload['source'] = Arr::get($pickupPayload, 'source', 'backend');
                $input['pickup_location_id'] = $pickupLocationId;
            } else {
                $selectedPickupLocation = null;
            }
        }
        $input['pickup'] = $pickupPayload;
        $transferDatetime = Arr::get($input, 'transfer_datetime');
        if (!$transferDatetime) {
            $fallbackDate = Arr::get($input, 'transfer_date');
            $fallbackTime = Arr::get($input, 'transfer_time');
            if ($fallbackDate && $fallbackTime) {
                try {
                    $transferDatetime = Carbon::parse($fallbackDate . ' ' . $fallbackTime, 'Asia/Tbilisi')
                        ->setTimezone('Asia/Tbilisi')
                        ->toIso8601String();
                    $input['transfer_datetime'] = $transferDatetime;
                    $request->merge(['transfer_datetime' => $transferDatetime]);
                } catch (\Exception $exception) {
                    $transferDatetime = null;
                }
            }
        }

        $query = $this->carClass->search($input);

        $dropoff = Arr::get($input, 'dropoff', []);
        if (is_string($dropoff)) {
            $decodedDropoff = json_decode($dropoff, true);
            if (json_last_error() === JSON_ERROR_NONE) {
                $dropoff = $decodedDropoff;
            }
        }
        if (!is_array($dropoff)) {
            $dropoff = [];
        }

        $userPickup = $request->input('user_pickup', []);
        if (is_string($userPickup)) {
            $decodedUserPickup = json_decode($userPickup, true);
            if (json_last_error() === JSON_ERROR_NONE) {
                $userPickup = $decodedUserPickup;
            }
        }
        if (!is_array($userPickup)) {
            $userPickup = [];
        }
        $input['user_pickup'] = $userPickup;
        $transferDate = null;
        if ($transferDatetime) {
            try {
                $transferDate = Carbon::parse($transferDatetime, 'Asia/Tbilisi')->setTimezone('Asia/Tbilisi')->toDateString();
            } catch (\Exception $exception) {
                $transferDate = null;
            }
        }
        $pickupLat = Arr::has($pickupPayload, 'lat') && is_numeric($pickupPayload['lat']) ? (float) $pickupPayload['lat'] : null;
        $pickupLng = Arr::has($pickupPayload, 'lng') && is_numeric($pickupPayload['lng']) ? (float) $pickupPayload['lng'] : null;
        $dropoffLat = Arr::has($dropoff, 'lat') && is_numeric($dropoff['lat']) ? (float) $dropoff['lat'] : null;
        $dropoffLng = Arr::has($dropoff, 'lng') && is_numeric($dropoff['lng']) ? (float) $dropoff['lng'] : null;
        $userPickupLat = Arr::has($userPickup, 'lat') && is_numeric($userPickup['lat']) ? (float) $userPickup['lat'] : null;
        $userPickupLng = Arr::has($userPickup, 'lng') && is_numeric($userPickup['lng']) ? (float) $userPickup['lng'] : null;
        $requiresTransfer = $pickupLat !== null && $pickupLng !== null && $dropoffLat !== null && $dropoffLng !== null;
        $transferDistanceKm = null;
        $transferDurationMin = null;
        $userPickupNormalized = null;
        if ($userPickupLat !== null && $userPickupLng !== null) {
            $userPickupNormalized = array_merge($userPickup, [
                'lat' => $userPickupLat,
                'lng' => $userPickupLng,
            ]);
        }
        if ($requiresTransfer) {
            $metrics = $this->carClass::resolveRouteMetrics($pickupPayload, $dropoff);
            $transferDistanceKm = $metrics['distance_km'];
            $transferDurationMin = $metrics['duration_min'];
            $passengers = (int) ($input['passengers'] ?? $input['number'] ?? $input['adults'] ?? 1);
            if ($passengers < 1) {
                $passengers = 1;
            }
            $userRouteMetrics = null;
            if ($userPickupNormalized) {
                $userRouteMetrics = $this->carClass::resolveRouteMetrics($userPickupNormalized, $dropoff);
            }
            $rows = $query->get();
            $filtered = $rows->filter(function ($car) use ($selectedPickupLocation, $pickupLocationId, $pickupPayload, $dropoff, $transferDistanceKm, $transferDurationMin, $transferDate, $transferDatetime, $passengers, $userPickupNormalized, $userRouteMetrics) {
                $pickupLocation = null;

                $userPayload = $userPickupNormalized;
                if ($car->pricing_mode !== 'fixed' && !$userPayload) {
                    $car->clearTransferContext();
                    return true;
                }

                $userPayload = $userPickupNormalized;
                if ($car->pricing_mode !== 'fixed' && !$userPayload) {
                    $car->clearTransferContext();
                    return true;
                }

                $userDistance = $userRouteMetrics['distance_km'] ?? null;
                $userDuration = $userRouteMetrics['duration_min'] ?? null;

                return $car->applyTransferContext(
                    $pickupLocation,
                    $pickupPayload,
                    $dropoff,
                    $transferDistanceKm,
                    $transferDurationMin,
                    $transferDate,
                    $transferDatetime,
                    $passengers,
